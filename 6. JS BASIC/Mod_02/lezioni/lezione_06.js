/* Lezione 06 - Gli operatori di confronto */

// Equality Operator on numbers (==)
let n1 = 5;
let n2 = 4;

console.log(n1 == n2);

// Loss Equality Operator on Strings (==)
n1 = "5";
console.log(n1 == n2);

// Strict Equality  (===)
console.log(n1 === n2);

// Greater than 
console.log(n1 > n2)

// Less than 
console.log(n1 < n2)

// Not equal
console.log(n1 != n2)

// Strict not equal 
console.log(n1 !== n2)

// Greater than or equal to
console.log(n1 >= n2)

// Less than or equal to
console.log(n1 <= n2)