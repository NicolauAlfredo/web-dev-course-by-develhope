const valore = 12;

// Verifica il tipo della variabile
if (typeof valore === "number") {
  console.log("The type of valore is a number");
} else if (typeof valore === "string") {
  console.log("The type of valore is a string");
} else if (typeof valore === "boolean") {
  console.log("The type of valore is a boolean");
} else {
  console.log("The type of valore is unknown");
}
